package _06_inheritance.exercise.design_and_impl_geometric;

public  class Shape {
    private  String color;

    public Shape() {
        this("bule");
    }

    public Shape(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "Shape{" +
                "color='" + color + '\'' +
                '}';
    }
}
